/* 
concat() - junta dois arrays

filter() - Retorna uma lista contando todos os elementos que passaram em um teste, 
ou seja, uma função escrita por nós.

find() - Funciona de forma parecida com o filter, porém retorna apenas o primeiro 
valor que satisfizer o teste, podendo ser uma string ou um número.

findIndex() - Funciona igual o find(), mas retorna o índice em vez do elemento, 
possibilitando usá-lo em outras partes do código.

lastIndexOf() - É igual o findIndex() porém começa do último elemento, não no primeiro.

forEach() - Executa uma função em cada elemento do array de forma individual. 

pop() - Retira o último elemento do array. Altera o array original removendo o elemento.

shift() - Retira o primeiro elemento do array.

push() - Adiciona o elemento passado como parâmetro do array, porém na última posição.

unshift() - Funciona igual ao push(), porém adiciona na primeira posição e acaba trocando 
o índice de todos os elementos.

reverse() - Inverte a ordem dos elementos do array, então o primeiro vira o último, 
o segundo o penúltimo e assim por diante.

slice() - Copia uma parte do array para outro array.

sort() - Organiza o array de acordo com a classificação Unicode, onde os números vêm 
antes das letras, porém não funciona corretamente para números, onde temos que definir 
uma função que irá auxiliar o comando.

splice() - Consegue remover, um ou mais elementos consecutivos caso o segundo parâmetro 
tenha um valor maior que 0, e incluir um ou mais elementos a partir de um índice escolhido.

*/